<h5 class="border-bottom border-gray pb-3 mb-2"><i class="fas fa-book"></i> Создание задачи</h5>
<p>Для создания новой задачи нажмите кнопку "Добавить задачу" в нижней части таблицы</p>
<div class='p-3 mb-3 HelpDeskImage'><img src='/images/helpdesk/add_task_1.jpg' alt = 'Добавить задачу' class="img-fluid"></div>
<p>Затем заполните все необходимые данные по задаче в Pop-Up окне и нажмите кнопку "Сохранить"</p>
<div class='p-3 mb-3 HelpDeskImage'><img src='/images/helpdesk/add_task_2.jpg' alt = 'Добавить задачу' class="img-fluid"></div>
<p>После сохранения, задача появится в списке доступных задач.</p>
<p>Для получение более подробной информации о задаче - кликните на названии задачи в списке. Вы попадете в пенель упраления задачей</p>
<div class='p-3 mb-3 HelpDeskImage'><img src='/images/helpdesk/add_task_3.jpg' alt = 'Редактор задачи' class="img-fluid"></div>
<p>В редакторе вы можете</p>
<ul>
	<li>Отредактировать описание задачи</li>
	<li>Добавить базовые изображения к задаче</li>
	<li>Вернуться в список всех задач</li>
	<li>Ознакомиться со списком креативов с составе задачи и дизайнеров, работающих с этими креативами</li>
</ul>
